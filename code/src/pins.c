
//Setup the pin multiplexers
setup_pins(void)
{
//P1.0/TA0CLK/ACLK/A0
	/// Set A0 as analog input (disable CMOS buffer)
	ADC10AE0 |= BIT0;
//P1.1/TA0.0/UCA0RXD/UCA0SOMI/A1
	/// UARTRXD
	P1SEL |= BIT1;
	P1SEL2|= BIT1;
//P1.2/TA0.1/UCA0TXD/UCA0SIMO/A2
	/// UARTTXD
	P1SEL |= BIT2;
	P1SEL2|= BIT2;
//P1.3/ADC10CLK/A3/VREF-/VEREF-
	/// Set A3 as analog input (disable CMOS buffer)
	ADC10AE0 |= BIT3;
//P1.4/SMCLK/UCB0STE/UCA0CLK/A4/VREF+/VEREF+/TCK
//P1.5/TA0.0/UCB0CLK/UCA0STE/A5/TMS
//P1.6/TA0.1/A6/UCB0SOMI/UCB0SCL/TDI/TCLK
	///SCL
	P1SEL |= BIT6;
	P1SEL2|= BIT6;
//P1.7/A7/UCB0SIMO/UCB0SDA/TDO/TDI
	///SDA
	P1SEL |= BIT7;
	P1SEL2|= BIT7;
//P2.0/TA1.0
	///~INT_ACCEL
	P2IE |= BIT0;
//P2.1/TA1.1
	///TOUCH_1
	P2IE |= BIT1;
//P2.2/TA1.1
	///TOUCH_2
	P2IE |= BIT2;
//P2.3/TA1.0
	///TOUCH_3
	P2IE |= BIT3;
//P2.4/TA1.2
	///SPEAKER
	P2DIR |= BIT4;
//P2.5/TA1.2
	///INT_CHG
	P2IE |= BIT5;
	P2IES|= BIT5;
//XIN/P2.6/TA0.1
	///LIGHT_CTRL_1
	P2DIR |= BIT6;
//XOUT/P2.7
	///LIGHT_CTRL_2
	P2DIR |= BIT7;
//P3.0/TA0.2
//P3.1/TA1.0
	///LED_1
	P3DIR |= BIT1;
//P3.2/TA1.1
	///LED_2
	P3DIR |= BIT2;
//P3.3/TA1.2
	///LED_3
	P3DIR |= BIT3;
//P3.4/TA0.0
	///R_CTRL
	P3DIR |= BIT4;
//P3.5/TA0.1
	///G_CTRL
	P3DIR |= BIT5;
//P3.6/TA0.2
	///B_CTRL
	P3DIR |= BIT6;
//P3.7/TA1CLK
	///VIBRATOR
	P3DIR |= BIT7;
//~RST~/NMI/SBWTDIO
	///SBWTDIO
//TEST/SBWTCK
	///SBWTCK
//DVCC
//DVSS
}